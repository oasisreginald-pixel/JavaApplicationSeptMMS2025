public class EquationB {
    public static void main(String[] args) {
        int x = 2 % 2 + 2 * 2 - 2 / 2;
        System.out.println("Step 1: 2 % 2 = 0");
        System.out.println("Step 2: 2 * 2 = 4");
        System.out.println("Step 3: 2 / 2 = 1");
        System.out.println("Step 4: 0 + 4 = 4");
        System.out.println("Step 5: 4 - 1 = 3");
        System.out.println("x = " + x);
    }
}