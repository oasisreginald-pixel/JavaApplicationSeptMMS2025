public class EquationA {
    public static void main(String[] args) {
        int x = 7 + 3 * 6 / 2 - 1;
        System.out.println("Step 1: 3 * 6 = 18");
        System.out.println("Step 2: 18 / 2 = 9");
        System.out.println("Step 3: 7 + 9 = 16");
        System.out.println("Step 4: 16 - 1 = 15");
        System.out.println("x = " + x);
    }
}